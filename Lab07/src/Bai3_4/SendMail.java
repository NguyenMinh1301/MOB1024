    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try {
            Properties properties = new Properties();
            properties.put("mail.smtp.auth", "true");
            properties.put("mail.smtp.starttls.enable", "true");
            properties.put("mail.smtp.host", "smtp.gmail.com");
            properties.put("mail.smtp.port", 587);

            String username = txtUser.getText().trim();
            String password = txtPass.getText().trim();
            String recipient = txtTo.getText().trim();
            String cc = txtMessage1.getText().trim();
            String subject = txtSubject.getText().trim();
            String messageBody = txtMessage.getText();

            Session session = Session.getInstance(properties, new Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(username));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(recipient));
            if (!cc.isEmpty()) {
                message.setRecipients(Message.RecipientType.CC, InternetAddress.parse(cc));
            }
            message.setSubject(subject);

            Multipart multipart = new MimeMultipart();

            MimeBodyPart textPart = new MimeBodyPart();
            textPart.setText(messageBody);
            multipart.addBodyPart(textPart);

            if (attachedFile != null) {
                MimeBodyPart filePart = new MimeBodyPart();
                FileDataSource source = new FileDataSource(attachedFile);
                filePart.setDataHandler(new DataHandler(source));
                filePart.setFileName(attachedFile.getName());
                multipart.addBodyPart(filePart);
            }

            message.setContent(multipart);
            Transport.send(message);

            JOptionPane.showMessageDialog(this, "Email đã được gửi thành công.");

        } catch (Exception ex) {
            Logger.getLogger(SendMail.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Không gửi được email: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSendActionPerformed

