    private void initScoreData() {
        DefaultTableModel model = (DefaultTableModel) tblScore.getModel();
        model.setRowCount(0);

        Service_Score service = new Service_Score();
        for (Model_Score s : service.getScoreData()) {
            model.addRow(new Object[]{
                s.getIdStudent(),
                s.getName(),
                s.getEnglish(),
                s.getComputer(),
                s.getPhysical()
            });
        }
    }

